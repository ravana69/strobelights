/*This script ads a class to the burger menu and dd menu when clicked  */
const menuBtn = document.querySelector('#menubtnborder');
var ddmenu = document.getElementById("menu");
let menuOpen = false;
menuBtn.addEventListener('click', () => {
  if(!menuOpen) {
    menuBtn.classList.add('open');
    ddmenu.classList.add('open');
    menuOpen = true;
  } else {
    menuBtn.classList.remove('open');
    ddmenu.classList.remove('open');
    menuOpen = false;
  }
});


